let couleur = "Rouge";

if(couleur.toLowerCase() == "rouge")
{
    console.log("Arrêtez vous!");
}
else if(couleur.toLowerCase() == "vert")
{
    console.log("Avancez!");
}
else if(couleur.toLowerCase() == "orange")
{
    console.log("Préparez vous à vous arrêter");
}
else
{
    console.log("C'est pas une couleur valide bouffon");
}
