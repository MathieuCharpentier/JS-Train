let numberArray = [1,2,3,4,5,6,7,8,9];

/**
 * take all number in a array, add them up and print the result in the console
 * @param {number Array} numbers a array of number
 */
function addAll(numbers)
{
    let result = 0;
    for(number of numbers)
    {
        result += number;
    }
    console.log("le resultat:"+result);
}

addAll(numberArray);